def clean_and_create_base():
    """Зашита дубликат: Агар папка аллакай вуҷуд дошта бошад, онро комилан тоза карда аз нав месозад"""
    if os.path.exists(BASE_RES_PATH):
        print(f"⚠️ Папкаи кӯҳна ёфт шуд. Барои пешгирии дубликат онро тоза мекунем...")
        shutil.rmtree(BASE_RES_PATH)
    os.makedirs(BASE_RES_PATH, exist_ok=True)
    print(f"✅ Папкаи нави тоза сохта шуд: {BASE_RES_PATH}")

def build_xml(lang_style):
    data = TEXT_DATA[lang_style]
    xml = '<?xml version="1.0" encoding="utf-8"?>\n<!-- 🛡️ АВТОМАТӢ СОХТА ШУД - ДУБЛИКАТ ЗАШИТА ФАЪОЛ АСТ -->\n<resources>\n'
    
    # Сатрҳои асосии тибқи стандарт
    xml += f'    <string name="prank_instruction_title">{data["instruction_title"]}</string>\n'
    xml += f'    <string name="prank_instruction_desc">{data["instruction_desc"]}</string>\n'
    xml += f'    <string name="prank_accept_btn">{data["accept_btn"]}</string>\n'
    xml += f'    <string name="prank_warning_bad_word">{data["warning_bad_word"]}</string>\n'
    xml += f'    <string name="prank_blocked_title">{data["blocked_title"]}</string>\n'
    xml += f'    <string name="prank_blocked_desc">{data["blocked_desc"]}</string>\n'
    xml += f'    <string name="prank_generation_empty">{data["generation_empty"]}</string>\n'
    xml += f'    <string name="prank_btn_replace">{data["btn_replace"]}</string>\n'
    xml += f'    <string name="prank_btn_force">{data["btn_force"]}</string>\n'
    xml += f'    <string name="prank_btn_cool">{data["btn_cool"]}</string>\n\n'
    
    xml += "    <!-- 20 КАТЕГОРИЯИ ОГОҲИИ ПРИКОЛӢ -->\n"
    for tag, title, desc in data["categories"]:
        # Экранизатсияи рамзҳо барои XML бехатарӣ
        t_clean = title.replace("'", "\\'").replace("&", "&amp;")
        d_clean = desc.replace("'", "\\'").replace("&", "&amp;")
        xml += f'    <string name="joke_{tag}_title">{t_clean}</string>\n'
        xml += f'    <string name="joke_{tag}_desc">{d_clean}</string>\n'
        
    xml += "</resources>"
    return xml

def main():
    print("🚀 Роботи универсалии Tajgram ба кор оғоз кард...")
    clean_and_create_base()
    
    for folder, lang_style in LANGUAGE_MAP.items():
        folder_full_path = os.path.join(BASE_RES_PATH, folder)
        os.makedirs(folder_full_path, exist_ok=True)
        
        file_path = os.path.join(folder_full_path, "strings_joke.xml")
        xml_content = build_xml(lang_style)
        
        with open(file_path, "w", encoding="utf-8") as f:
            f.write(xml_content)
            
        print(f"📦 [Успешно] {folder}/strings_joke.xml сохта шуд ({lang_style})")
        
    print("\n🎉 ТАМОМ! Ҳамаи минтақаҳои ҷаҳонӣ ҷой ба ҷой шуданд. Лоиҳа 100% ҳимоя ва омода аст!")

if __name__ == "__main__":
    main()
